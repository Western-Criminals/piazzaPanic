public class Dish {
    private String name;
    private int duration;

    public void setDishName(String name) {
        this.name = name;
    }

    public void setDuration(int duration) {
        this.duration = duration;
    }

    public String getDishName() {
        return this.name;
    }

    public String getDuration() {
        return this.duration;
    }
}
